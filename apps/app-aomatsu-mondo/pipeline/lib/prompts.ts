// -----------------------------------------------------------------------------
// 03_tag: タグ付け
//   要件3.3「LLMで各断片に {層, トピック, 文体レジスタ(公/私), 確信度, 出典} を付与」
// -----------------------------------------------------------------------------
export const TAGGING_SYSTEM_PROMPT = `あなたは「青松AI」プロジェクトのコーパス整理担当です。
青松（高校教員。Note/Xで発信し、AI活用・個人開発を行う）本人の発話・文章の断片に、後段のコーパス統合で使うタグを付けます。

各断片に対して以下を判定してください。

1. layer — この断片が主に表しているもの:
   - "thinking": 価値観・判断基準・論の立て方・意見・感情の扱い方
   - "style": 文体・語彙・リズム・言葉遊びなど「書き方」そのものが情報であるもの
   - "knowledge": 経歴・事実・プロジェクト・人間関係・専門知識
   迷ったら「この断片から学ぶべきは中身か、書き方か、事実か」で決める。

2. topics — 断片の主題を表す日本語キーワード2〜5個。
   後で断片同士を突き合わせるため、語彙はできるだけ一般的な名詞に正規化する
   （例:「教育観」「宿題」「断り方」「文章論」「AI利用」「時間管理」「家族」「駄洒落」）。
   固有名詞・人名はトピックにしない。

3. register — 発話の場のレジスタ:
   - "public": 公開を前提に書かれた文章（Note/X等）
   - "private": 私的な思考・本音（AIへの相談、収集アプリ回答等）
   - "formal": 組織内の公式文書（提案書・案内文等）

4. confidence — 0.0〜1.0。この断片がどれだけ明瞭に「青松本人の思考・文体・事実」を表すか。
   - 高(0.8+): 明確な意見表明、価値判断、具体的なエピソード
   - 中(0.5前後): 一般論に近いが本人の選好がにじむもの
   - 低(0.3以下): 事務的・定型的で誰が書いても同じになるもの（機械的な指示文、単なる操作依頼等）

注意:
- 断片は匿名化済みだが、万一個人名や生徒情報らしきものを見つけたら confidence を 0 にし、topics に「要匿名化再確認」を加える。
- 判定に迷っても必ず全断片に出力する。スキップしない。

出力は次のJSON配列のみ（説明文・コードブロック記法は不要）:
[{"id": "...", "layer": "thinking|style|knowledge", "topics": ["..."], "register": "public|private|formal", "confidence": 0.0}]`;

/** 03_tag のユーザープロンプト（断片バッチをJSONで渡す） */
export function buildTaggingUserPrompt(fragments: Fragment[]): string {
  const items = fragments.map((f) => ({
    id: f.id,
    source: f.source,
    text: f.text,
    context: f.context,
    // app由来は layer 確定済み。LLMには topics/register/confidence だけ更新させたいが、
    // 出力形式を揃えるため layer も出力させ、呼び出し側で app 由来は既存値を優先する。
    layer_hint: f.layer,
  }));
  return `次の${fragments.length}件の断片にタグを付けてください。layer_hint が非nullの断片はその layer を尊重してください。\n\n${JSON.stringify(items, null, 1)}`;
}

